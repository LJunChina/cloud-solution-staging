CREATE TABLE transaction_message(
  CREATE_AT DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL COMMENT '创建时间',
  CREATE_BY VARCHAR(128) NOT NULL DEFAULT 'SYSTEM' COMMENT '创建人',
  UPDATE_AT DATETIME COMMENT '更新时间',
  UPDATE_BY VARCHAR(128) COMMENT '更新人',
  ID VARCHAR(32) NOT NULL DEFAULT '' COMMENT '主键',
  MESSAGE_ID VARCHAR(32) NOT NULL DEFAULT '' COMMENT '消息ID',
  MESSAGE_BODY TEXT NOT NULL COMMENT '消息内容',
  MESSAGE_DATA_TYPE VARCHAR(128) COMMENT '消息类型',
  CONSUMER_QUEUE VARCHAR(128) NOT NULL COMMENT '消息队列',
  MESSAGE_RE_SEND_TIMES INT NOT NULL DEFAULT 0 COMMENT '消息重发次数',
  VERSION INT NOT NULL DEFAULT 0 COMMENT '消息版本号',
  IS_DEAD VARCHAR(1) DEFAULT '0' NOT NULL COMMENT '是否已死亡 1-是0-否',
  STATUS VARCHAR(2) DEFAULT '00' NOT NULL COMMENT '消息状态 00-待确认 01-发送中 02-已发送',
  REMARK VARCHAR(1000) COMMENT '备注信息',
  PRIMARY KEY (ID)
) COMMENT '消息队列信息';
CREATE INDEX idx_msg_message_id ON transaction_message(MESSAGE_ID);